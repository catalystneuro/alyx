% Find the name/wire number of all channels that were within 
% 1 mm above or below CA1 from Spock on April 27 2017.

% Set this variable with the monkey id
subject_nickname = 'laureano'
stl_name = 'ca1'
date = '2020-11-11'
% To query a year set variable year with the year you want to query:
year = '2020'
